# 연산자

# 연산자 우선순위
print((10 - 2) * 4 / 2)
# 우선순위가 높은 연산자가 먼저 연산된다.
# 우선순위가 같으면 왼쪽 -> 오른쪽으로 연산된다.
# () 소괄호가 우선순위가 가장 높다.

# 0. ()         소괄호
# 1. **         거듭제곱
# 2. * / // %   곱셈, 나눗셈
# 3. + -        덧셈, 뺄셈
# 4. == != < > <= >= 비교연산
# 5. not        부정로직
# 6. and        AND로직
# 7. or         OR로직
# 8. = += -=    대입(복합대입)
